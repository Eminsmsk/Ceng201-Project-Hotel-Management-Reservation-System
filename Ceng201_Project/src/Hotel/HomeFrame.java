    private void bt_diningHall_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_diningHall_addActionPerformed
        if(combo_diningHall_add.getSelectedIndex()==0){
            for (int i = 0; i < added; i++) {
                SystemClass.guestsInRoom(roomNo).get(i).setDiningHall(true);
            }
            addedToDiningHall.add(combo_diningHall_add.getSelectedIndex());
        }
        else{
            if(addedToDiningHall.contains(0)){
                JOptionPane.showMessageDialog(this, "<html><font face='Segoe UI' size='12' color='green'>You you've already added all the guests!");
            }
            else{
                for (int i = 1; i < added+1; i++) {
                    if(combo_diningHall_add.getSelectedIndex()==i){
                        if(!addedToDiningHall.contains(combo_diningHall_add.getSelectedIndex())){
                            SystemClass.guestsInRoom(roomNo).get(combo_diningHall_add.getSelectedIndex()-1).setDiningHall(true);
                            addedToDiningHall.add(combo_diningHall_add.getSelectedIndex());
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "<html><font face='Segoe UI' size='12' color='red'>You cannot add the guest that you've already added!");
                    }
                    }
                }
                
            }
 
        }
            
      
        
       
    }//GEN-LAST:event_bt_diningHall_addActionPerformed

