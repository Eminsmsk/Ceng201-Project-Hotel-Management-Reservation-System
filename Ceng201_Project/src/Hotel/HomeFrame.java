    private void search_bt_searchGuestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_bt_searchGuestActionPerformed
        String name = search_tf_name.getText().trim();
        String surname = search_tf_surname.getText().trim();
        String id = search_tf_nationalID.getText().trim();
        
        
        if(!id.isEmpty()){
           Guest temp = SystemClass.findGuest(id);
           if(temp!=null){
               search_textArea.setText(temp.toString());
           }
           else{
               JOptionPane.showMessageDialog(this, "<html><font face='Segoe UI' size='12' color='red'> There is no guest with that ID!");
               search_textArea.setText("");
           }
            
        }
        else if(!name.isEmpty() && !surname.isEmpty()){
            ArrayList<Guest> temp = SystemClass.findGuest(name, surname);
            if(temp!=null){
                String msg = "";
                for (int i = 0; i < temp.size(); i++) {
                  msg+= temp.get(i).toString();  
                }
                search_textArea.setText(msg);
            }
            else{
                JOptionPane.showMessageDialog(this, "<html><font face='Segoe UI' size='12' color='red'> There is no guest with that name and surname!");
                search_textArea.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "<html><font face='Segoe UI' size='12' color='red'> Fill in the required fields!");
            search_textArea.setText("");
        }
        



      
    }//GEN-LAST:event_search_bt_searchGuestActionPerformed

